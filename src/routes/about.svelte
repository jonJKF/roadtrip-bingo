    <h1>About</h1>
    <p>This project is a last-minute submission to Cloudflare's 2021 Summer Developer Challenge!</p>
    <p>It is based on an idea my sister and I had many years ago for entertaining kids in a car or on a train
        that had since been forgotten until she reminded me while brainstorming ideas for a fun project
        to enter in the Developer Challenge!</p>
    <p>It also served as an opportunity to try out Svelte, Sveltekit, Vite and Tailwind with the new JIT compiler which I had been meaning to try for a while!</p>
    <p>The Cloudflare technologies in use are:</p>
    <ul>
        <li>DNS (of course!)</li>
        <li>Pages</li>
        <li>Workers for the API</li>
        <li>Workers KV for the "collectables" on the board</li>
        <li>Durable Objects for the game sessions</li>
    </ul>
